public class calculadora {

    public static int soma(int num1, int num2){
        return num1+num2;
    }

    public static int subtrair(int num1, int num2){
        return num1-num2;

    }

    public static int dividir(int num1, int num2){

        return num1 / num2;
    }

    public static int multiplicar(int num1, int num2){

        return num1*num2;
    }

}
